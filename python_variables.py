# Phayton variables
age = 30
print("Integer:", age)

# Float 
height = 1.75
print("Float:", height)

# String 
language = "Pythonista"
print("String:", language)

# List 
colors = ["red", "green", "blue"]
print("List:", colors)

# Tuple 
location = (12, 8)
print("Tuple:", location)

# Dictionary 
student = {"first_name": "John", "grade": 11}
print("Dictionary:", student)

# Boolean 
has_passed = False
print("Boolean:", has_passed)

# Set 
lucky_numbers = {7, 14, 21}
print("Set:", lucky_numbers)

# Type Checking
print("Type of age:", type(age))
print("Type of language:", type(language))
